    public static class PieChart_AWT extends ApplicationFrame 
    {   
        public PieChart_AWT( String title ) {
           super( title ); 
           setContentPane(createDemoPanel( ));
        }

        private static PieDataset createDataset( ) {
           DefaultPieDataset dataset = new DefaultPieDataset( );
           dataset.setValue( "No Diabetes" , new Double( UploadDataset.noDi ) );  
           dataset.setValue( "Diabetes" , new Double( UploadDataset.preDi ) );   
           dataset.setValue( "Chronic kidney disease" , new Double( UploadDataset.Di ) );                 
           return dataset;         
        }

        private static JFreeChart createChart( PieDataset dataset ) {
           JFreeChart chart = ChartFactory.createPieChart(      
              "OUTPUT",   // chart title 
              dataset,          // data    
              true,             // include legend   
              true, 
              false);
           
           PiePlot plot = (PiePlot) chart.getPlot();
           plot.setLabelGenerator(new StandardPieSectionLabelGenerator("{0} {2}"));
           
           return chart;
        }

        public static JPanel createDemoPanel( ) {
           JFreeChart chart = createChart(createDataset( ) );  
           return new ChartPanel( chart ); 
        }
       
     }
